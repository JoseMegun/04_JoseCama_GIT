
package S07_04;

public class Videojuego_04 {
    // Atributos
    String nombre;
    int lanzamiento;
    
    // Constructor
    public Videojuego_04(String nombre, int lanzamiento) {
    this.nombre = nombre;
    this.lanzamiento = lanzamiento;
    }

    // Metodos
    public String getNombre() {
        return nombre;
    }

    public int getLanzamiento() {
        return lanzamiento;
    }
}

