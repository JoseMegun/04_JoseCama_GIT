
package S08_04;

public class Deporte_04 {
    private String nombre;
    private int añoFundacion;

    public Deporte_04(String nombre, int añoFundacion) {
        this.nombre = nombre;
        this.añoFundacion = añoFundacion;
    }

    public String getNombre() {
        return nombre;
    }

    public int getAñoFundacion() {
        return añoFundacion;
    }
}

