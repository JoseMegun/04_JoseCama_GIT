
package S08_04;

public class Deporte_04 {
    // Atributos
    String nombre;
    int añoFundacion;

    // Constructor del class Deporte_04
    public Deporte_04(String nombre, int añoFundacion) {
        this.nombre = nombre;
        this.añoFundacion = añoFundacion;
    }

    // Metodos
    public String getNombre() {
        return nombre;
    }

    public int getAñoFundacion() {
        return añoFundacion;
    }
}

