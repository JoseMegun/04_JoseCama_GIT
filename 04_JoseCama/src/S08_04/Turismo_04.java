
package S08_04;

public class Turismo_04 {
    // Atributos
    String destino;
    int añoInicio;

    // Constructor del class Turismo_04
    public Turismo_04(String destino, int añoInicio) {
        this.destino = destino;
        this.añoInicio = añoInicio;
    }

    // Metodos
    public String getDestino() {
        return destino;
    }

    public int getAñoInicio() {
        return añoInicio;
    }
}